package api;

public class Weather {
	
	private int temp;
	private String wfkor;
	private int hour;
	
	public int getTemp() {
		return temp;
	}
	public void setTemp(int temp) {
		this.temp = temp;
	}
	public String getWfkor() {
		return wfkor;
	}
	public void setWfkor(String wfkor) {
		this.wfkor = wfkor;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
}
